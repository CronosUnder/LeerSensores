    public Main() {
        //Inicia los componentes del panel
        initComponents();
        UtilDispositivos dispUtil = UtilDispositivos.getInstance();

        this.addWindowListener(new WindowAdapter() {
            public void windowClosing(WindowEvent e) {
                int i = JOptionPane.showConfirmDialog(null, "Al salir se desconectaan todos los dispocitivos", "Â¿Esta seguro que quiere salir?", WIDTH);
                if (i == 0) {
                    System.exit(0);//cierra aplicacion
                }
            }
        });
        log("obteniendo puertos\n");
        DefaultTableModel model = (DefaultTableModel) jTableDispositivos.getModel();
        for (int i = 0; i < dispUtil.getDispNames().length; i++) {
            log("abriendo puerto " + dispUtil.getDispNames()[i] + "  ");
            try {
                dispUtil.conectarPuerto(dispUtil.getDispNames()[i]);
                log(dispUtil.getSerialPorts().get(dispUtil.getDispNames()[i]).isOpened() + "  \n");
                model.addRow(new Object[]{dispUtil.getDispNames()[i], "Conectado"});
            } catch (SerialPortException e) {
                log("error abriendo puerto " + dispUtil.getDispNames()[i] + " \n");
                model.addRow(new Object[]{dispUtil.getDispNames()[i], e.getExceptionType()});
                log(e.getMessage());

            }
        }

    }

